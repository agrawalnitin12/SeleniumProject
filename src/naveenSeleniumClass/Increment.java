package naveenSeleniumClass;

public class Increment {

	public static void main(String[] args) {
		
		int a = 10;
		int b = a++;
		
		System.out.println(a +","+ b);
		
		int i = 10;
		int j = ++i;
		System.out.println(i+","+j);
		
		int m = 10;
		int n = m--;
		System.out.println(m+","+n);
		
		int x = 10;
		int y = --x;
		System.out.println(x+","+y);

	}

}
